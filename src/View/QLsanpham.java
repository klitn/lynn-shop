    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        String Name=txtTensp.getText();
        String Masp=txtMasp.getText();
        if(Name.length()>0){
            Sanpham = new DAOsp().findbyName(Name);
            Model.setRowCount(0);
            for(sanpham s:Sanpham){
                Model.addRow(new Object[]{
                    TableResult.getRowCount()+1,s.getMasp(),s.getTensp(),s.getLoai(),s.getSoluong(),s.getGia()
                });
            }
        }

        if(Masp.length()>0){
            Sanpham = new DAOsp().findbyMasp(Masp);
            Model.setRowCount(0);
            for(sanpham s:Sanpham){
                Model.addRow(new Object[]{
                    TableResult.getRowCount()+1,s.getMasp(),s.getTensp(),s.getLoai(),s.getSoluong(),s.getGia()
                });
            }
        }
        
        if(Name.equals("") && Masp.equals("")){
            JOptionPane.showMessageDialog(this, "Hãy nhập mã sản phẩm hoặc tên bạn muốn tìm!");
        }
    }//GEN-LAST:event_btnFindActionPerformed

