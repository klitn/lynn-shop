    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try {
            selectedIndex=TableResult.getSelectedRow();
                sanpham s = Sanpham.get(selectedIndex);
                new DAOsp().DeleteSP(s.getId());
        if(Sanpham.isEmpty()){
            JOptionPane.showMessageDialog(this, "Không có sản phẩm để sửa!");
        }
        
        else{
            sanpham sp = Sanpham.get(selectedIndex);
            new DAOsp().DeleteSP(sp.getId());
            sp.setMasp(txtMasp.getText());
            sp.setTensp(txtTensp.getText());
            sp.setLoai(ComboxKind.getSelectedItem().toString());
            
            
             boolean isOK = true;
        try {
            sp.setGia(Float.parseFloat(txtGia.getText()));
            sp.setSoluong(Integer.parseInt(txtsolg.getText()));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Là số không phải là kí tự");
            isOK =false;
        }
        if(isOK){
        new DAOsp().AddSP(sp);
        showTable();
        JOptionPane.showMessageDialog(this, "Sửa thành công");
        }
        
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Hãy chọn một dòng rồi sửa!","Thông báo",JOptionPane.WARNING_MESSAGE);
        }
                
    }//GEN-LAST:event_btnEditActionPerformed

